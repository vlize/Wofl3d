#include "libft.h"

void	ft_putnbr(int nb)
{
	char	c;

	c = 48;
	if ((nb < 0) && (nb > -2147483648))
	{
		ft_putchar('-');
		nb = -nb;
	}
	else if (nb == -2147483648)
		return (ft_putstr("-2147483648"));
	if (nb < 10)
	{
		c += nb;
		ft_putchar(c);
	}
	else
	{
		ft_putnbr(nb / 10);
		c += nb % 10;
		ft_putchar(c);
	}
}
