#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;
	char	*ptr;

	i = 0;
	ptr = NULL;
	while (s[i] != '\0')
	{
		if (s[i] == c)
		{
			ptr = (char *)&s[i];
		}
		i++;
	}
	if ((s[i] == '\0') && (c == '\0'))
		ptr = (char *)&s[i];
	return (ptr);
}
