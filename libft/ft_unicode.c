void	ft_unicode0(int wc, int *n)
{
	if (wc < 0x800)
		n[1] = ((wc / 0X40) & 0x1F) + 0xC0;
	else if (wc < 0x10000)
	{
		n[1] = ((wc / 0X40) & 0x3F) + 0x80;
		n[2] = ((wc / 0X1000) & 0x0F) + 0xE0;
		n[4] = 3;
	}
	else if (wc < 0x200000)
	{
		n[1] = ((wc / 0X40) & 0x3F) + 0x80;
		n[2] = ((wc / 0X1000) & 0x3F) + 0x80;
		n[3] = ((wc / 0X40000) & 0x07) + 0xF0;
		n[4] = 4;
	}
}

void	ft_unicode(int wc, int *n)
{
	n[1] = 0;
	n[2] = 0;
	n[3] = 0;
	n[4] = 1;
	if (wc < 0x80)
		n[0] = wc;
	else
	{
		n[0] = (wc & 0x3F) + 0x80;
		n[4] = 2;
		return (ft_unicode0(wc, n));
	}
}
