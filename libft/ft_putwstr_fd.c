#include "libft.h"

void	ft_putwstr_fd(wchar_t const *ws, int fd)
{
	size_t	i;

	i = 0;
	if (!ws)
		return ;
	while (ws[i] != '\0')
	{
		ft_putwchar_fd(ws[i], fd);
		i++;
	}
}
