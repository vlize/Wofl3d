#include "libft.h"

void	ft_putwchar_fd(wchar_t wc, int fd)
{
	int	n[5];

	ft_unicode(wc, n);
	while (n[4] > 0)
	{
		write(fd, &n[0], 1);
		n[4]--;
	}
}
