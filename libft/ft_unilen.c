int	ft_unilen(int wc)
{
	if ((wc < 0x80) && (wc >= 0))
		return (1);
	else if (wc < 0x800)
		return (2);
	else if (wc < 0x10000)
		return (3);
	else if (wc < 0x200000)
		return (4);
	return (0);
}
