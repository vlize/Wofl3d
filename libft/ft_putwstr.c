#include "libft.h"

void	ft_putwstr(wchar_t const *ws)
{
	size_t	i;

	i = 0;
	if (!ws)
		return ;
	while (ws[i] != '\0')
	{
		ft_putwchar(ws[i]);
		i++;
	}
}
