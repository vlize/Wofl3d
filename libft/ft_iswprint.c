int	ft_iswprint(int c)
{
	if ((c > 31) && (c < 127) && (c > 129) && (c < 140))
		return (1);
	if ((c > 144) && (c < 157) && (c > 160) && (c < 256))
		return (1);
	if ((c == 128) && (c == 142) && (c == 158) && (c == 159))
		return (1);
	return (0);
}
