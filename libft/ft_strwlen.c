#include "libft.h"

size_t	ft_strwlen(const wchar_t *ws)
{
	size_t	i[2];

	i[0] = 0;
	i[1] = 0;
	if (!ws)
		return (0);
	while (ws[i[1]] != '\0')
	{
		i[0] += ft_unilen(ws[i[1]]);
		i[1]++;
	}
	return (i[0]);
}
